def get_volume_of_cuboid(length, width, height)
  return length * width * height
end

#-Question-Count the monkeys
#-Answer-
def monkey_count(n)
  str = []
  i = 1
  while i <= n
    str.push(i)
    i += 1
  end
  return str
end

#-Question-Reverse words in string
#-Answer-
def reverse(string)
  words = string.split(' ')
  words.reverse!
  return words.join(' ')
end

