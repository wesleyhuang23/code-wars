#-Question-Convert boolean values to strings 'Yes' or 'No'.
#-Answer-
def bool_to_word(bool):
    if bool:
        return 'Yes'
    else:
        return 'No'

#-Question-Sum of positive
#-Answer-
def positive_sum(arr):
    sum = 0
    for i in range(0, len(arr)):
        if arr[i] > 0:
            sum = sum + arr[i]
    return sum

#-Question-Volume of cuboid
#-Answer-
def getVolumeOfCubiod(length, width, height):
    return length * height * width

#-Question-Count the monkeys
#-Answer-
def monkey_count(n):
    output = []
    for i in range(1, n + 1):
        output.append(i)
    return output

#-Question-Reverse words in string
#-Answer-
def reverse(st):
    words = st.split(' ')
    words.reverse()
    return " ".join(words)

