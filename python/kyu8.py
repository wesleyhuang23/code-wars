def greet(name):
    if name == "Johnny":
        return "Hello, my love!"
    else:
        return "Hello, %s!" % (name)

#-Question-Plural
#-Answer-
def plural(n):
    if n != 1:
        return True
    elif n == 1:
        return False

#-Question-Even or odd
#-Answer-
def even_or_odd(number):
    if number % 2 == 0:
        return "Even"
    elif number % 2 != 0:
        return "Odd"

#-Question-calculate average
#-Answer-
def find_average(array):
    if len(array) == 0:
        return 0
    else:
        sum = 0
        for i in range(0, len(array)):
            sum = sum + array[i]
        return sum / len(array)

