#-Question-Sum of numbers from 0 to N
#-Answer-
def show_sequence(n):
    output = ""
    sum = 0
    if n == 0:
        return '0=0'
    elif n < 0:
        return '%s<0' % (n)
    for i in range(0, n + 1):
        if i == n:
            output = output + str(i)
            sum = sum + i
        else:
            output = output + str(i) + '+'
            sum = sum + i
    output = output + " = " + str(sum)
    return output

